CREATE TABLE company (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ticker VARCHAR(10) NOT NULL,
    name VARCHAR(255) NOT NULL,
    exchange VARCHAR(64),
    sector VARCHAR(128),
    industry VARCHAR(128),
    country VARCHAR(128),
    website_url VARCHAR(2048),
    description TEXT,
    logo_url VARCHAR(2048),
    provider_symbol VARCHAR(64) NOT NULL,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    CONSTRAINT uk_company_ticker UNIQUE (ticker),
    CONSTRAINT ck_company_ticker_format CHECK (ticker ~ '^[A-Z][A-Z0-9.-]{0,9}$'),
    CONSTRAINT ck_company_name_not_blank CHECK (btrim(name) <> ''),
    CONSTRAINT ck_company_provider_symbol_not_blank CHECK (btrim(provider_symbol) <> ''),
    CONSTRAINT ck_company_timestamps CHECK (updated_at >= created_at)
);
